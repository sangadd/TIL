package array;

public class Array1 {
    public static void main(String[] args) {
        int student1 = 90;
        int student2 = 80;
        int student3 = 70;
        int student4 = 60;
        int student5 = 50;

        System.out.println(student1);
        System.out.println(student2);
        System.out.println(student3);
        System.out.println(student4);
        System.out.println(student5);

        /* 학생을 더 추가한다면 코딩 양이 증가하는 문제 발생
        * 반복문으로 학생이 누군지 (학생1, 학생2, ...) 해결할 수 있을 것 같지만
        * 점수를 출력하는 부분에는 변수의 이름이 다르기 때문에 반복문 적용 어려움
        *
        * 그래서 같은 타입의 변수를 반복해서 선언하고 반복해서 사용하는 문제를 해결하는 것이 배열임 !! */
    }
}
